#include <iostream>

using namespace std;

int main() {
    // basic data types
    int aNum = 5;              // Integer (whole number without decimals)
    float myFloatNum = 9.95;   // Floating point number (with decimals) but 4 bytes of memory (under 8 decimals)
    double myFloatNum = 5.99;  // Floating point number (with decimals) but 8 bytes of memory (>8 decimals)
    char myLetter = 'D';       // Character
    string myText = "Hello";   // String (remember, a string is just a string of chars)
    bool myBoolean = true;     // Boolean (true or false)

    // It is possible to define a float with scientific notation
    float f1 = 35e3;
    double d1 = 12E4;

}